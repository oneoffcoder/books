package com.oneoffcoder.java.operators;

public class OperatorCompoundAssignment {

  public static void main(String[] args) throws Exception {
    int a = 10;
    a = a + 4;

    int b = 10;
    b += 4;

    int c = 10;
    c = c - 4;

    int d = 10;
    d -= 4;

    int e = 10;
    e = e * 2;

    int f = 10;
    f *= 2;

    int g = 10;
    g = g / 2;

    int h = 10;
    h /= 2;
  }

}
